export class alimentos{
    public nome_alimento: string;
    public quantidade_alimento: number;
    public unidade: string;

    constructor(nome_alimento: string, quantidade_alimento: number, unidade: string){
        this.nome_alimento = nome_alimento;
        this.quantidade_alimento = quantidade_alimento;
        this.unidade = unidade;
    }




}